export type Topic = {
  id: string;
  title: string;
  order: number;
};

export type Course = {
  [key: string]: Topic[];
};

export type Curriculum = {
  TYT: Course;
  AYT: Course;
};

function topics(idPrefix: string, titles: string[]): Topic[] {
  return titles.map((title, index) => ({
    id: `${idPrefix}-${index + 1}`,
    title,
    order: index + 1,
  }));
}

